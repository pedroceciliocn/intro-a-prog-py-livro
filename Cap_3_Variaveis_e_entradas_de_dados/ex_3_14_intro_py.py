distancia = float(input("De a distancia (em km) percorrida com o carro: "))
dias = float(input("De a quantidade de dias que o carro foi usado: "))

preço = (60 * dias) + (0.15 * distancia) 
print(f"O preço a ser pago é de R${preço:.2f}.")
                     
                     
                     
